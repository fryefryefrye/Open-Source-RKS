bool GpsUpdate(unsigned char k)
{
	static unsigned char SepIndex = 0;
	static unsigned char LastSeconds = 0;
	if (GPRMC_command ==1)
	{
		if (k == ',')
		{
			SepIndex++;
			ElementIndex = 0;
			return false;
		}

		switch(SepIndex)
		{
		case 1:
			if (ElementIndex < 6)
			{
				GPS_Time[ElementIndex] = k;
			}
			ElementIndex ++;
			break;

		case 2:
			GPS_Valid[ElementIndex] = k;
			ElementIndex ++;
			break;


		case 9:

			GPS_Date[ElementIndex] = k;
			ElementIndex ++;

			break;
		}

		if (SepIndex > 11)
		{
			GPRMC_Index = 0	;
			GPRMC_command = 0;
			SepIndex = 0;



			if (LastSeconds!=GPS_Time[5])
			{
				LastSeconds=GPS_Time[5];
				OnSeconds();
				return true;
			} 
			else
			{
				return false;
			}



		}
	}
	else
	{
		if (GPRMC_command_array[GPRMC_Index] == k)
		{
			GPRMC_Index++;
			if (GPRMC_Index>5)
			{
				GPRMC_command =1;
			}
		}
		else
		{
			GPRMC_Index=0;
		}
	}
	return false;
}

